// Funci√≥n auxiliar para navegar a √úPlay de manera robusta
async function navigateToUPlay(page: any) {
  console.log('üéØ Navegando a √úPlay de manera robusta...');
  
  // Lista de selectores basados en la estructura Material UI real
  const uplaySelectors = [
    // Selectores espec√≠ficos para Material UI ListItemButton
    '[aria-label="Navegar a √úPlay"]',
    '.MuiListItemButton-root:has-text("√úPlay")',
    '.MuiListItem-root:has-text("√úPlay") .MuiListItemButton-root',
    'div[role="button"]:has-text("√úPlay")',
    
    // Selectores de respaldo tradicionales
    'a[href="/uplay"]',
    'a[href*="uplay"]',
    'nav a:has-text("√úPlay")',
    'button:has-text("√úPlay")',
    '[data-testid*="uplay"]',
    
    // Selectores por contenido de texto
    'text="√úPlay"',
    ':has-text("√úPlay")'
  ];
  
  // Intentar cada selector
  for (const selector of uplaySelectors) {
    try {
      const element = page.locator(selector).first();
      if (await element.isVisible({ timeout: 2000 })) {
        console.log(`‚úÖ Enlace √úPlay encontrado con selector: ${selector}`);
        await element.click();
        await page.waitForLoadState('networkidle');
        console.log('‚úÖ Navegaci√≥n a √úPlay completada');
        return;
      } else {
        console.log(`‚ÑπÔ∏è  Selector ${selector} no encontrado, probando siguiente...`);
      }
    } catch (error) {
      console.log(`‚ÑπÔ∏è  Selector ${selector} no encontrado, probando siguiente...`);
    }
  }
  
  // Si no encuentra ning√∫n enlace, intentar navegaci√≥n directa
  console.log('‚ö†Ô∏è  No se encontr√≥ enlace √úPlay, navegando directamente...');
  await page.goto('/uplay');
  await page.waitForLoadState('networkidle');
}

